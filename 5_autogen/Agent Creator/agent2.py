from autogen_core import MessageContext, RoutedAgent, message_handler
from autogen_agentchat.agents import AssistantAgent
from autogen_agentchat.messages import TextMessage
from autogen_ext.models.openai import OpenAIChatCompletionClient
import messages
import random


class Agent(RoutedAgent):

    system_message = """
    You are a technology-savvy real estate innovator. Your mission is to leverage Agentic AI to create novel solutions in the real estate market or enhance existing services.
    Your personal interests are in these sectors: Real Estate, Financial Services.
    You are inspired by ideas that foster transformation and improve customer experiences.
    You tend to shy away from ideas that focus solely on traditional practices.
    You are analytical, detail-oriented, and a prudent risk-taker. You enjoy crafting solutions that are practical and impactful.
    Your weaknesses: you're very meticulous, which can sometimes slow you down.
    You should communicate your real estate solutions clearly and persuasively to engage stakeholders.
    """

    CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER = 0.4

    def __init__(self, name) -> None:
        super().__init__(name)
        model_client = OpenAIChatCompletionClient(model="gpt-4.1-nano", temperature=0.65)
        self._delegate = AssistantAgent(name, model_client=model_client, system_message=self.system_message)

    @message_handler
    async def handle_message(self, message: messages.Message, ctx: MessageContext) -> messages.Message:
        print(f"{self.id.type}: Received message")
        text_message = TextMessage(content=message.content, source="user")
        response = await self._delegate.on_messages([text_message], ctx.cancellation_token)
        idea = response.chat_message.content
        if random.random() < self.CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER:
            recipient = messages.find_recipient()
            message = f"Here is my real estate innovation idea. It may not be your specialty, but please refine it and enhance it. {idea}"
            response = await self.send_message(messages.Message(content=message), recipient)
            idea = response.content
        return messages.Message(content=idea)